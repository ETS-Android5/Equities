package airhawk.com.myapplication;

public class Constructor_Ipos {
    String ipo_name;
    String ipo_message;
    String ipo_startDate;
    String ipo_endDate;

    public String getIpo_name() {
        return ipo_name;
    }

    public void setIpo_name(String ipo_name) {
        this.ipo_name = ipo_name;
    }

    public String getIpo_message() {
        return ipo_message;
    }

    public void setIpo_message(String ipo_message) {
        this.ipo_message = ipo_message;
    }

    public String getIpo_startDate() {
        return ipo_startDate;
    }

    public void setIpo_startDate(String ipo_startDate) {
        this.ipo_startDate = ipo_startDate;
    }

    public String getIpo_endDate() {
        return ipo_endDate;
    }

    public void setIpo_endDate(String ipo_endDate) {
        this.ipo_endDate = ipo_endDate;
    }
}
